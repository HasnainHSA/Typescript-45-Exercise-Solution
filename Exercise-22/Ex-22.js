// Test 1: Equality (==)
var apples = 5;
var oranges = 5;
console.log("Are apples equal to oranges? I predict True.");
console.log(apples == oranges); // Should be true
// Test 2: Inequality (!=)
var bananas = 3;
var pineapples = 7;
console.log(bananas != pineapples); // Should be true
// Test 3: Checking if an array is empty
var fruits = [];
console.log(fruits.length === 0);
// Test 4: Strict inequality (!==)
var UnluckyNumber = 22;
var luckyNumber = 8;
console.log(UnluckyNumber !== luckyNumber);
// Test 5: Greater than (>)
var giraffes = 8;
var elephants = 10;
console.log(elephants > giraffes);
// Test 6: Less than (<)
var zebras = 15;
var kangaroos = 20;
console.log(kangaroos < zebras);
// Test 7: Greater than or equal to (>=)
var lions = 13;
var tigers = 12;
console.log(lions >= tigers);
// Test 8: Less than or equal to (<=)
var penguins = 40;
var polarBears = 35;
console.log(penguins <= polarBears);
// Test 9: Logical NOT (!)
var isNight = true;
console.log(!isNight);
// Test 10: Logical AND (&&)
var sunnyDay = true;
var warmTemperature = false;
console.log(sunnyDay && warmTemperature);
// Test 11: Logical OR (||)
var rainyDay = false;
var umbrellaAvailable = false;
console.log(rainyDay || umbrellaAvailable);
